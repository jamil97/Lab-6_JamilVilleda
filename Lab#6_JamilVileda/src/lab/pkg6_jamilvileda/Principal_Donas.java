    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        JFileChooser jfc = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Archivo de Texto", "txt");
        jfc.addChoosableFileFilter(filtro);
        int seleccion = jfc.showSaveDialog(this);
        FileWriter fw = null;
        BufferedWriter bw = null;
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            f = new Factura("./factura.txt", tf_correoelectronico.getText(), tf_nombrecliente.getText(), tf_rtn.getText(), tf_cai.getText(), tf_fechaactual.getText());
            f.EscribirArchivo(f);
            JOptionPane.showMessageDialog(this, "La factura se ha guardado correctamente");
            this.jd_facturacion.dispose();
        }


    }//GEN-LAST:event_jButton2MouseClicked

