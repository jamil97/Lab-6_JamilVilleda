    private void bt_agregarbebidaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bt_agregarbebidaMouseClicked

        double precioventa = Double.parseDouble(tf_precioventa.getText());
        double preciocosto = Double.parseDouble(tf_costoproducto.getText());
        String nombreproducto = tf_nombreproducto.getText();
        String tamañobebida = tf_tamaño.getText();
        String estadobebida = tf_estadobebida.getText();
        String tipobebida = tf_bebida.getText();

        inv.getProducto().add(new Bebidas(estadobebida, tamañobebida, tipobebida, preciocosto, precioventa, nombreproducto));
        f.getProductos().add(new Bebidas(estadobebida, tamañobebida, tipobebida, preciocosto, precioventa, nombreproducto));
        inv.EscribirArchivo();
        JOptionPane.showMessageDialog(jd_comida, "Se ha guardado exitosamente la bebida. ");

    }//GEN-LAST:event_bt_agregarbebidaMouseClicked

